  function initLangSwitcher(buttonId, menuId, labelId) {
    const langBtn = document.getElementById(buttonId);
    const langMenu = document.getElementById(menuId);
  
    if (!langBtn || !langMenu) return;
  
    langBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      langMenu.classList.toggle('hidden');
    });
  
    langMenu.addEventListener('click', (e) => {
      const selectedLang = e.target.getAttribute('data-lang');
      if (!selectedLang) return;
  
      updateTexts(selectedLang);
      localStorage.setItem('siteLang', selectedLang);
      langMenu.classList.add('hidden');
    });
  
    document.addEventListener('click', () => {
      langMenu.classList.add('hidden');
    });
  }
  
